package org.example;

/**
 * Calcula el área de un triángulo. Valor base: 4. Valor altura: 3.
 * @version 1.0
 * @author Carlos Costas
 */

public class Main {
    public static void main(String[] args) {
        int base, altura, area;
        base = 4;
        altura = 3;
        area = base * altura / 2;
        System.out.println(area);
    }
}
