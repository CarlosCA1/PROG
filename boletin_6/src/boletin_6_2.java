public class boletin_6_2 {

    public static void eco(int n) {
        for (int i = 0; i < n; i++) {
            System.out.println("Ecoo...");
        }
    }


    public static void main(String[] args) {
        int veces = 5; // Ejemplo: Número de veces que se repetirá el mensaje
        eco(veces);    // Llamada a la función
    }
}